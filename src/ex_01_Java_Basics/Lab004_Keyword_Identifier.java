package ex_01_Java_Basics;

public class Lab004_Keyword_Identifier {
    public static void main(String[] args) {
        System.out.println("Hello World, How many number of Identifiers and Keywords are there?");
        //Note-How to identify them?
        /*
        Words which are in blue color
        and are in lowercase are Keywords which are reserved in java
        whereas
        The names of our class and packages we use which are unique
        are known as identifiers.
         */
        System.out.println("There are 5 Keywords = package, public, class, static, void");
        System.out.println("There are 3 Identifiers = ex_01_Java_Basics, Lab004_Keyword_Identifier, args");
    }
}
