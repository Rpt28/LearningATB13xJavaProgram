package ex_11_WhileLoop;

public class Lab047_While {
    public static void main(String[] args) {
        int i = 0;
        while (i < 10) {
            System.out.println(i);
            i++;
        }

        //  I, C, U
        int j = 0;
        for (; j < 10; j++) {
            System.out.println(j);
        }

        int modi = 1;
        while (modi<=15){
            System.out.println("Modi will do 15 years");
            modi++;
        }
    }
}
