package ex_15_String_Builder_Buffer;

public class Lab068_String_Buffer_Builder {
    public static void main(String[] args) {
        String str1 = "Rahul";
        String str2 = new String("Prakash");
        StringBuffer str3 = new StringBuffer("Tiwari");
        StringBuilder str4 = new StringBuilder("RPT");
        System.out.println(str1);
        System.out.println(str2);
        System.out.println(str3);
        System.out.println(str4);
    }
}
